#[test]
fn test_non_incremental_kem() {
    let sk = SK.try_into().unwrap();
    let pk = PK.try_into().unwrap();

    let (ct, ss) = mlkem768::encapsulate(&pk, RANDOMNESS);
    assert_eq!(ct.as_slice(), &CT);
    assert_eq!(ss, SS);

    let ss2 = mlkem768::decapsulate(&sk, &ct);

    assert_eq!(ss, ss2);
}

#[test]
#[cfg(feature = "incremental")]
fn test_incremental_kem() {
    use libcrux_sha3::sha256;
    let pk2: &[u8; mlkem768::incremental::pk2_len()] =
        &PK[..mlkem768::incremental::pk2_len()].try_into().unwrap();
    let mut pk1 = [0u8; mlkem768::incremental::pk1_len()];
    pk1[..32].copy_from_slice(&PK[mlkem768::incremental::pk2_len()..]);

    let hash = sha256(&PK);
    pk1[32..].copy_from_slice(&hash);

    let mut state = [0u8; mlkem768::incremental::encaps_state_len()];
    let mut ss = [0u8; mlkem768::incremental::shared_secret_size()];

    let ct1 = mlkem768::incremental::encapsulate1(pk1.as_slice(), RANDOMNESS, &mut state, &mut ss)
        .unwrap();
    assert_eq!(state, STATE1);
    let ct2 = mlkem768::incremental::encapsulate2(&state, pk2);
    assert_eq!(state, STATE2);

    assert_eq!(ss, SS);
    assert_eq!(ct1.value, CT[..mlkem768::incremental::Ciphertext1::len()]);
    assert_eq!(ct2.value, CT[mlkem768::incremental::Ciphertext1::len()..]);
}
