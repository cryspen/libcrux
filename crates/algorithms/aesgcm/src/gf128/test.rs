use super::*;

fn gf128<T: GF128FieldElement>(key: &[u8], input: &[u8], out: &mut [u8]) {
    debug_assert!(key.len() == 16);
    debug_assert!(out.len() == 16);

    let mut st = GF128State::<T>::init(key);
    st.update_padded(input);
    st.emit(out);
}

