#[test]
fn test_gf128() {
    let mut computed: [u8; 16] = [0u8; 16];
    gf128::<crate::platform::portable::FieldElement>(&KEY, &INPUT, &mut computed);
    for i in 0..16 {
        if computed[i] != EXPECTED[i] {
            #[cfg(feature = "std")]
            std::eprintln!(
                "mismatch at {}: expected is {}, computed is {}",
                i,
                EXPECTED[i],
                computed[i]
            );
            panic!();
        }
    }
}

